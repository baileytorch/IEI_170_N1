# AND Y
print("Operador lógico AND o Y, Verdadero cuando ambos valores son verdaderos.")
print(True & True)
print(False & True)
print(True & False)
print(False & False)

# OR O
print("Operador lógico OR u O, Verdadero cuando uno de los valores es verdadero.")
print(True | True)
print(False | True)
print(True | False)
print(False | False)

# NOT 
print("Operador lógico NOT, niega un valor.")
print(not True)
print(not False)